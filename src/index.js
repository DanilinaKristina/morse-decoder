const MORSE_TABLE = {
  '.-': 'a',
  '-...': 'b',
  '-.-.': 'c',
  '-..': 'd',
  '.': 'e',
  '..-.': 'f',
  '--.': 'g',
  '....': 'h',
  '..': 'i',
  '.---': 'j',
  '-.-': 'k',
  '.-..': 'l',
  '--': 'm',
  '-.': 'n',
  '---': 'o',
  '.--.': 'p',
  '--.-': 'q',
  '.-.': 'r',
  '...': 's',
  '-': 't',
  '..-': 'u',
  '...-': 'v',
  '.--': 'w',
  '-..-': 'x',
  '-.--': 'y',
  '--..': 'z',
  '.----': '1',
  '..---': '2',
  '...--': '3',
  '....-': '4',
  '.....': '5',
  '-....': '6',
  '--...': '7',
  '---..': '8',
  '----.': '9',
  '-----': '0',
};

module.exports = function decode(expr) {
  const chunks = expr.match(/.{10}/g); // разбиваем на блоки по 10 символов
  return chunks
    .map((block) => {
      if (block === '**********') return ' ';
      let morse = '';
      for (let i = 0; i < 10; i += 2) {
        const pair = block.slice(i, i + 2);
        if (pair === '10') morse += '.';
        else if (pair === '11') morse += '-';
      }
      return MORSE_TABLE[morse];
    })
    .join('');
};
